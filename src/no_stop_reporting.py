"""Assembles the final Arabic report for the no-stop-loss scenario test.
Code/columns stay English; narrative is Arabic, same convention as the
rest of this project's reporting modules.
"""
from src import config


def _fmt(x, digits=3):
    return f"{x:.{digits}f}" if x == x else "غير متوفر"


def _fmt_pct(x, digits=1):
    return f"{x*100:.{digits}f}%" if x == x else "غير متوفر"


def _pf(x):
    return "∞" if x == float("inf") else _fmt(x, 3)


def build_report(results: dict) -> str:
    summary = results["summary"]
    tail = results["tail_risk"]
    recovery = results["recovery"]
    sig = results["significance"]
    boot = results["bootstrap"]
    shuffle = results["shuffle"]

    baseline = summary[summary["config"].str.contains("with stop-loss")].iloc[0]
    no_sl = summary[summary["config"].str.contains("no stop-loss")].iloc[0]

    lines = []
    lines.append("# تقرير اختبار \"بلا وقف خسارة\" (No Stop-Loss) — NAS100\n")
    lines.append(
        "هذا تحليل افتراضي (test-only) بحت، ردًا على سؤال مباشر: ماذا لو لم يُستخدم وقف الخسارة "
        "(`initalSL`) إطلاقًا، وتُركت الصفقة تتحرك حرة حتى تُغلق فعليًا؟ لم يُعدَّل أي شيء في السجل الأصلي "
        f"(`analytics_1.csv`) — هذا محاكاة بديلة بحتة على نفس الـ{baseline['n']} صفقة.\n"
    )

    lines.append("## القاعدة المستخدمة في المحاكاة\n")
    lines.append(
        "بدون وقف خسارة، الصفقة تقنيًا ممكن تبقى مفتوحة إلى ما لا نهاية — فاحتجنا قاعدة إنهاء موثَّقة: "
        f"كل صفقة تُترك تتحرك على الشموع الحقيقية لغاية **{config.NO_STOP_MAX_EXTENSION_MINUTES//(24*60)} يومًا** "
        "من لحظة الدخول (بدون أي هدف ربح أو تدخّل)، وتُقيَّم عند إغلاق آخر شمعة في هذه النافذة — أيًا كان "
        "السعر حينها، ربحًا أو خسارة. اختيرت مدة 30 يومًا لأن 6 صفقات فقط من أصل 789 لديها بيانات شموع أقل "
        f"من هذه المدة بعد دخولها (تُحتسب كـ\"بيانات منتهية\" ولا يُفترض فيها شيء).\n\n"
        "تتبّعنا أيضًا **أسوأ نقطة وصلتها الصفقة أثناء الرحلة** (`no_sl_worst_r_reached`) — حتى لو تعافت "
        "لاحقًا، لأن هذا الرقم هو ما يهم فعليًا لإدارة المخاطر (رصيد الحساب لا يعرف أن الصفقة ستتعافى لاحقًا).\n"
    )

    lines.append("## الملخص التنفيذي\n")
    lines.append(
        f"**الإجابة المباشرة على السؤال: لا، إزالة وقف الخسارة فكرة خطيرة جدًا — لكن القصة أكثر تعقيدًا من "
        f"\"دائمًا أسوأ\".**\n\n"
        f"- على المستوى **الفردي/المتوسط**: الصفقة النموذجية (الوسيط، median) كانت لتنتهي عند "
        f"**{_fmt(no_sl['median'])}R** بلا وقف مقابل **{_fmt(baseline['median'])}R** فعليًا (أغلب الصفقات "
        f"الأصلية تُغلق عند -1R تمامًا بسبب الوقف). معدل الفوز كان يرتفع من "
        f"{_fmt_pct(baseline['win_rate'])} إلى {_fmt_pct(no_sl['win_rate'])} — أي أن **أكثر من نصف الصفقات "
        f"المتوقفة فعليًا كانت لتتعافى وتربح لو أُعطيت وقتًا ومساحة أكبر** "
        f"({recovery['n_would_have_recovered']} من {recovery['n_original_stop_outs']}، "
        f"{_fmt_pct(recovery['pct_would_have_recovered'])}, بعائد وسيط {_fmt(recovery['median_recovery_r'])}R عند التعافي).\n"
        f"- لكن على مستوى **الحساب ككل**: هذا لا يهم إطلاقًا أمام خطر الذيل. **{tail['n_catastrophic']}** صفقة "
        f"من أصل {tail['n']} ({_fmt_pct(tail['pct_catastrophic'])}) لامست في مرحلة ما خسارة أعمق من "
        f"{config.NO_STOP_CATASTROPHIC_R_THRESHOLD}R (3 أضعاف وحدة المخاطرة العادية). أسوأ صفقة منفردة "
        f"وصلت إلى **{_fmt(tail['min_worst_r_reached'])}R** — أي ما يقارب "
        f"{abs(tail['min_worst_r_reached'])*500:,.0f}$ خسارة نظرية على صفقة واحدة (بوحدة مخاطرة ~500$)، "
        "وهي صفقة دخلت شراء يومين قبل انهيار كوفيد-19 في فبراير 2020 بوقف ضيق جدًا (6.5 نقطة فقط).\n"
        f"- **الأثر التراكمي على الحساب**: منحنى الأداء التراكمي بلا وقف وصل إلى تراجع أقصى "
        f"(max drawdown) قدره **{_fmt(no_sl['max_drawdown_r'], 0)}R** مقابل {_fmt(baseline['max_drawdown_r'], 0)}R "
        "فقط مع الوقف الفعلي — أي أن استخدام وقف الخسارة، رغم أنه يقطع بعض الصفقات التي كانت ستتعافى، هو ما "
        "أبقى الحساب حيًا أصلاً.\n"
    )

    lines.append("## جدول المقارنة الكامل\n")
    lines.append("| الإعداد | n | المتوسط (R) | الوسيط (R) | معدل الفوز | معامل الربح | أقصى تراجع (R) | أسوأ صفقة (R) |")
    lines.append("|---|---|---|---|---|---|---|---|")
    for _, r in summary.iterrows():
        lines.append(f"| {r['config']} | {int(r['n'])} | {_fmt(r['mean'])} | {_fmt(r['median'])} | "
                     f"{_fmt_pct(r['win_rate'])} | {_pf(r['profit_factor'])} | {_fmt(r['max_drawdown_r'], 1)} | "
                     f"{_fmt(r['worst_single_trade_r'], 2)} |")
    lines.append(
        "\n**لماذا المتوسط (mean) مضلِّل هنا**: عدد قليل جدًا من الصفقات الكارثية (أسوأها -407R) يسحب "
        "المتوسط بعيدًا؛ الوسيط (median) أكثر تمثيلًا للصفقة \"النموذجية\"، لكن القرار الصحيح لا يُبنى على "
        "أي منهما بمفرده — بل على توزيع المخاطر الكاملة أدناه.\n"
    )

    lines.append("## توزيع مخاطر الذيل (Tail Risk)\n")
    lines.append(
        f"- الوسيط لأسوأ نقطة وصلتها أي صفقة: **{_fmt(tail['median_worst_r_reached'])}R**\n"
        f"- أسوأ 5% من الصفقات لامست على الأقل: **{_fmt(tail['p5_worst_r_reached'])}R**\n"
        f"- أسوأ 1% من الصفقات لامست على الأقل: **{_fmt(tail['p1_worst_r_reached'])}R**\n"
        f"- عدد الصفقات \"الكارثية\" (أعمق من {config.NO_STOP_CATASTROPHIC_R_THRESHOLD}R في مرحلة ما): "
        f"**{tail['n_catastrophic']}** ({_fmt_pct(tail['pct_catastrophic'])} من كل الصفقات)\n"
        f"- من بين هذه الصفقات الكارثية، **{tail['n_recovered_after_catastrophic']}** انتهت بربح رغم كل شيء "
        "— يعني حتى \"التعافي\" لا يمنع أن الحساب كان ليمر بلحظة خسارة عميقة جدًا في الطريق.\n"
        f"- **{tail['n_data_censored']}** صفقات قرب نهاية البيانات (2024) لم تُكمل نافذة الـ30 يومًا كاملة "
        "بسبب انتهاء بيانات الشموع المتوفرة — عولجت كبيانات ناقصة، وليست نتيجة مؤكدة.\n"
    )

    lines.append("## هل الفرق دال إحصائيًا على مستوى الصفقة الواحدة؟\n")
    lines.append(
        f"اختبار مزدوج (paired) بين العائد الأصلي وعائد \"بلا وقف\" على نفس {sig['n']} صفقة:\n\n"
        f"- t-test: t={_fmt(sig['t_statistic'])}, p={_fmt(sig['t_p_value'], 4)}\n"
        f"- Wilcoxon signed-rank (أنسب هنا لأن التوزيع شديد الانحراف بسبب القيم المتطرفة): "
        f"p={_fmt(sig['wilcoxon_p_value'], 4)}\n\n"
        "**كلا الاختبارين لا يُظهران دلالة إحصائية على مستوى \"الصفقة النموذجية\"** — وهذا متوقَّع ومنطقي: "
        "معظم الصفقات لا فرق كبير فيها (أو حتى تتحسن)، والفرق الحقيقي يأتي من عدد قليل جدًا من الصفقات "
        "الكارثية التي تُغرق أي اختبار يعتمد على \"المتوسط النموذجي\". هذا بالضبط سبب وجود وقف الخسارة أصلاً: "
        "**الحماية من الذيل النادر، وليس تحسين الصفقة المتوسطة**.\n"
    )

    lines.append("## أثر إزالة الوقف على الحساب ككل (Monte Carlo)\n")
    lines.append(
        f"Bootstrap على إجمالي العائد التراكمي (إعادة تشكيل الصفقات {config.MC_ITERATIONS} مرة):\n\n"
        f"- الإجمالي الفعلي (بالوقف): {_fmt(boot['observed_total_orig_r'], 1)}R\n"
        f"- الإجمالي الافتراضي (بلا وقف): {_fmt(boot['observed_total_no_sl_r'], 1)}R\n"
        f"- متوسط الفرق عبر Bootstrap: {_fmt(boot['mean_bootstrap_diff'], 1)}R "
        f"(فترة ثقة 95%: {_fmt(boot['diff_ci_lo_2.5pct'], 0)}R إلى {_fmt(boot['diff_ci_hi_97.5pct'], 0)}R)\n"
        f"- نسبة مرات Bootstrap التي كانت فيها \"بلا وقف\" أسوأ من \"بالوقف\": "
        f"**{_fmt_pct(boot['pct_boot_iters_no_sl_worse'])}**\n\n"
        "فترة الثقة واسعة جدًا (تشمل الصفر) — لأن نتيجة أي 789 صفقة بلا وقف تعتمد بشدة على هل تضمّنت أحداثًا "
        "كارثية نادرة (مثل كوفيد) أم لا. هذا في حد ذاته تحذير: النتيجة **غير مستقرة وعالية التقلب**، وهذا "
        "بالضبط ما يعنيه \"خطر غير محسوب\".\n"
    )

    lines.append("## هل كان التوقيت الفعلي للصفقات سيء الحظ، أم أن هذا متوقَّع بأي ترتيب؟\n")
    lines.append(
        f"أُعيد ترتيب نفس الصفقات عشوائيًا {2000} مرة (المجموع الكلي للعائد لا يتغير بالترتيب، لكن أقصى "
        "تراجع نعم يتغير):\n\n"
        f"- أقصى تراجع بالترتيب الزمني الفعلي: **{_fmt(shuffle['actual_chronological_max_drawdown_r'], 0)}R**\n"
        f"- وسيط أقصى تراجع عبر إعادات الترتيب العشوائية: {_fmt(shuffle['shuffled_median_max_drawdown_r'], 0)}R\n"
        f"- نسبة الترتيبات العشوائية التي كانت أسوأ من الترتيب الفعلي: "
        f"**{_fmt_pct(shuffle['pct_shuffles_worse_than_actual'])}**\n\n"
        + (
            "الترتيب الزمني الفعلي كان **أسوأ من أغلب الترتيبات العشوائية الممكنة** — منطقي: بعض الأحداث "
            "الكارثية (كوفيد 2020، وصفقتان متتاليتان في يوليو 2024) تجمّعت زمنيًا بدل أن تتوزع، ما ضاعف "
            "عمق التراجع. لكن حتى الوسيط بين كل الترتيبات العشوائية "
            f"({_fmt(shuffle['shuffled_median_max_drawdown_r'], 0)}R) يبقى تراجعًا كارثيًا على أي حساب.\n"
            if shuffle["pct_shuffles_worse_than_actual"] < 0.5 else
            "الترتيب الزمني الفعلي لم يكن أسوأ بشكل استثنائي — أي أن هذا المستوى من التراجع الكارثي متوقَّع "
            "تقريبًا بأي ترتيب زمني للصفقات نفسها، وليس مجرد سوء حظ في التوقيت.\n"
        )
    )

    lines.append("## تحفّظ مهم جدًا: هذا سيناريو نظري بحت\n")
    lines.append(
        "الأرقام أعلاه (خسائر تصل لمئات أضعاف وحدة المخاطرة على صفقة واحدة) **لن تحدث فعليًا بهذا الشكل عند "
        "وسيط تداول حقيقي**: أي حساب برافعة مالية سيتلقى **نداء هامش (margin call)** ويُغلَق قسريًا من "
        "الوسيط قبل الوصول لهذا المستوى من الخسارة بوقت طويل. هذه الأرقام تُظهر **اتجاه وحجم المخاطرة "
        "غير المحدودة** لو تُرك السعر يتحرك حرًا، وليست تنبؤًا حرفيًا بخسارة دولارية فعلية ممكنة — الرسالة "
        "العملية واحدة في الحالتين: **بدون وقف خسارة، حجم الخسارة غير محدود وغير قابل للتحكم**.\n"
    )

    lines.append("## الخلاصة\n")
    lines.append(
        "وقف الخسارة الحالي **ليس تفصيلًا يمكن الاستغناء عنه لتحسين الأداء** — إزالته قد تُحسّن الصفقة "
        "المتوسطة (لأن أكثر من نصف الإيقافات كانت مبكرة جدًا وكانت لتتعافى)، لكنها تفتح الباب أمام خسارة "
        "غير محدودة في عدد قليل من الصفقات كافٍ لتدمير الحساب بالكامل عدة مرات. **التوصية**: أي تحسين لجودة "
        "الخروج (كما في تحليل الوقف المتحرك وتحسين الخروج حسب النمط) يجب أن يبقى دائمًا فوق وقف خسارة فعّال "
        "كخط أحمر، وليس بديلاً عنه.\n"
    )

    return "\n".join(lines)
