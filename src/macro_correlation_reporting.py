"""Assembles the final Arabic report for the macro/cross-asset correlation
test (test-only; answers a direct question about gold/dollar/EUR/VIX/
equity-market composite patterns). Code/columns stay English; narrative is
Arabic, same convention as the rest of this project's reporting modules.
"""
from src import config, external_data


def _fmt(x, digits=3):
    return f"{x:.{digits}f}" if x == x else "غير متوفر"


def _fmt_pct(x, digits=1):
    return f"{x*100:.{digits}f}%" if x == x else "غير متوفر"


SOURCE_ROWS = [
    ("VIX (مؤشر الخوف)", "VIXCLS", "FRED (بنك الاحتياطي الفيدرالي)", "رسمي", "يومي"),
    ("قوة الدولار (تقريبي)", "DTWEXBGS", "FRED", "رسمي — ليس DXY الحقيقي، بديل تقريبي معلَن", "يومي"),
    ("يورو/دولار", "DEXUSEU", "FRED", "رسمي (بيانات H.10 للفيدرالي)", "يومي"),
    ("S&P 500 (بديل \"أرباح الشركات\")", "SP500", "FRED", "رسمي — مؤشر سعري وليس بيانات أرباح مجمّعة فعلية", "يومي"),
    ("الذهب (عقود COMEX الآجلة)", "GC=F", "Yahoo Finance", "غير رسمي — أفضل مصدر مجاني متاح (سلسلة LBMA الرسمية بـFRED متوقفة منذ 2015)", "يومي"),
]


def build_report(results: dict) -> str:
    scored = results["scored"]
    corr = results["sanity_correlations"]
    n_tested = results["n_tested"]
    n_survivors = int((scored["reject_fdr"]).sum()) if len(scored) else 0

    lines = []
    lines.append("# تقرير: هل توجد أنماط مركّبة مرتبطة بالذهب/الدولار/اليورو/VIX/S&P 500؟ — NAS100\n")
    lines.append(
        "إجابة مباشرة على سؤال محدد: هل يبحث النظام عن ارتباطات مركّبة بين صفقات NAS100 وأصول خارجية "
        "(ذهب، مؤشر الدولار، يورو/دولار، VIX، أرباح الشركات)؟ هذا تحليل افتراضي منفصل تمامًا عن خط الأنابيب "
        "الأساسي (الذي يُبنى عمدًا من بيانات NAS100 الداخلية فقط، حسب الطلب الأصلي)، ولا يعدّل أي بيانات "
        f"أصلية. اختُبرت **{n_tested}** توليفة شرط على نفس 789 صفقة.\n"
    )

    lines.append("## مصادر البيانات (الدقة والمصداقية أولاً)\n")
    lines.append("| المؤشر | المعرّف | المصدر | الحالة | التكرار |")
    lines.append("|---|---|---|---|---|")
    for row in SOURCE_ROWS:
        lines.append(f"| {row[0]} | `{row[1]}` | {row[2]} | {row[3]} | {row[4]} |")
    lines.append(
        "\n**كل الميزات محسوبة بقيمة اليوم السابق فقط** (`_asof_prior_day`) بنفس منهجية عدم تسريب المعلومات "
        "المستقبلية المتّبعة في باقي المشروع — صفقة دخلت يوم الثلاثاء لا ترى إلا بيانات الاثنين وما قبله.\n"
    )

    lines.append("## فحص سلامة أولي (Sanity Check) — قبل البحث عن أنماط\n")
    lines.append(
        "ارتباط بسيط (Pearson) بين العائد اليومي لـNAS100 والعائد اليومي لكل مؤشر، عبر 1,298 يوم تداول "
        "مشترك — هذا **ليس** جزءًا من اختبار الأنماط الرسمي، فقط تأكيد أن البيانات الخارجية صحيحة ومحاذاة "
        "زمنيًا بشكل سليم (نتائج معروفة تاريخيًا، فإذا خرجت بشكل غريب لكان مؤشرًا على خطأ بالبيانات):\n"
    )
    lines.append("| المؤشر | الارتباط مع عائد NAS100 اليومي | منطقي؟ |")
    lines.append("|---|---|---|")
    interp = {
        "vix_ret": "منطقي جدًا — الخوف يرتفع مع هبوط الأسهم",
        "sp500_ret": "منطقي جدًا — مؤشرا أسهم أمريكيين مرتبطان ببعض",
        "dxy_proxy_ret": "منطقي — الدولار القوي يضغط عادة على الأصول الخطرة",
        "eurusd_ret": "منطقي — عكس اتجاه الدولار تقريبًا",
        "gold_ret": "ضعيف كما متوقَّع — الذهب أقل ارتباطًا مباشرًا بأسهم التكنولوجيا",
    }
    labels = {"vix_ret": "VIX", "sp500_ret": "S&P 500", "dxy_proxy_ret": "الدولار (تقريبي)",
              "eurusd_ret": "يورو/دولار", "gold_ret": "الذهب"}
    for key, label in labels.items():
        lines.append(f"| {label} | {_fmt(corr[key])} | {interp[key]} |")
    lines.append("")

    lines.append("## نتيجة البحث عن أنماط مركّبة (الاختبار الرسمي)\n")
    if n_survivors == 0:
        lines.append(
            f"**لا يوجد أي نمط مركّب ناجٍ.** من أصل {n_tested} توليفة (مفردة وثنائية) بين المؤشرات "
            "الخارجية الخمسة، صفر منها نجا من تصحيح Bonferroni ضمن هذه العائلة المُسجَّلة مسبقًا "
            "(alpha=0.05). هذه نتيجة سلبية صادقة: **الارتباط العام على مستوى المؤشر** (كما ظهر أعلاه، "
            "خاصة مع VIX وS&P 500) **لا يعني وجود قاعدة تداول مركّبة موثوقة** على مستوى الصفقة الفردية "
            "بهذا حجم العيّنة.\n"
        )
    else:
        lines.append(f"**{n_survivors}** نمطًا نجا من تصحيح Bonferroni:\n")
        for _, r in scored[scored["reject_fdr"]].iterrows():
            lines.append(f"- `{r['condition']}` (n={r['n']}, عائد متوقع={_fmt(r['expectancy'])}R)")
    lines.append("")

    if len(scored):
        lines.append("### أقرب المرشَّحات (لم تنجُ، لكنها الأقرب لدلالة أولية)\n")
        lines.append("| الشرط | n | العائد المتوقع (R) | p الخام | p بعد Bonferroni | مونت كارلو |")
        lines.append("|---|---|---|---|---|---|")
        for _, r in scored.head(8).iterrows():
            lines.append(f"| `{r['condition']}` | {r['n']} | {_fmt(r['expectancy'])} | "
                         f"{_fmt(r['p_value_expectancy'], 4)} | {_fmt(r['p_bonferroni'], 3)} | {r['mc_verdict']} |")
        lines.append(
            "\nكل هذه المرشَّحات في فئة \"استكشافي/مرفوض\" — عُرضت للشفافية (لا نُظهر فقط ما نجح)، وليس "
            "كتوصية.\n"
        )

    lines.append("## تحفّظات جوهرية\n")
    lines.append(
        "- **تناقض التردد**: المؤشرات الخارجية يومية، بينما دخول الصفقات بفاصل دقيقة — أي حركة داخل اليوم "
        "بعد إغلاق المؤشر الخارجي غير مرئية للنموذج.\n"
        "- **الذهب من مصدر غير رسمي** (Yahoo Finance) لأن السلسلة الرسمية بـFRED متوقفة — النتائج "
        "المرتبطة بالذهب تحديدًا تستحق حذرًا إضافيًا.\n"
        "- **\"أرباح الشركات\" ≈ مؤشر S&P 500 السعري فقط** — لا يوجد مصدر مجاني رسمي ينشر بيانات أرباح "
        "مجمّعة يومية؛ هذا بديل عملي شائع وليس نفس الشيء حرفيًا.\n"
        "- **حجم العيّنة**: 789 صفقة مقسَّمة على 489 توليفة — نفس التحذير المتكرر عبر كل هذا المشروع: "
        "طبقات تجزئة إضافية (نمط داخلي × نمط خارجي) تصغّر كل خانة بسرعة.\n"
        "- **DTWEXBGS ليس DXY الحقيقي** — بديل رسمي مجاني موثَّق صراحة كتقريب، حسب طلب الملف الوصفي الأصلي.\n"
    )

    lines.append("## الخلاصة\n")
    lines.append(
        "نعم، بُنيت آلية بحث كاملة عن أنماط مركّبة مع أصول خارجية (ذهب، دولار، يورو، VIX، S&P500) بنفس "
        "صرامة المنهجية المستخدمة بكل التحليلات السابقة (walk-forward + مونت كارلو + تصحيح إحصائي + أدلة "
        "بايزية). **النتيجة: لا دليل كافٍ حتى الآن على وجود نمط مركّب موثوق** على مستوى الصفقة الفردية، "
        "رغم أن الارتباطات العامة على مستوى المؤشر (خصوصًا VIX) منطقية ومتوقَّعة. هذا لا ينفي وجود علاقة "
        "حقيقية أضعف من أن تُكتشف بـ789 صفقة فقط — فقط يعني أنها غير مؤكَّدة إحصائيًا بالبيانات الحالية.\n"
    )

    lines.append("## الخطوات التالية المقترحة\n")
    lines.append(
        "1. جمع المزيد من الصفقات قبل إعادة اختبار هذا الاتجاه — 489 توليفة على 789 صفقة عبء إحصائي كبير.\n"
        "2. لو توفّر اشتراك مدفوع لبيانات ذهب رسمية (مثل LBMA عبر Nasdaq Data Link)، يستحق استبدال مصدر "
        "Yahoo غير الرسمي به لأي قرار مهم.\n"
        "3. اختبار المؤشرات الخارجية بفاصل زمني أدق من يومي (بالساعة مثلًا) إن توفّر مصدر مجاني موثوق، "
        "لتقليل فجوة التردد مع صفقات NAS100 بالدقيقة.\n"
    )

    return "\n".join(lines)
